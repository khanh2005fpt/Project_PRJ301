    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        response.setContentType("text/html;charset=UTF-8");
        String cateID = request.getParameter("cateid");
        String index = request.getParameter("index");
        ProductDao pd = new ProductDao();
        CategoryDao cd = new CategoryDao();
        List<Category> listC = cd.getAllCategory();
        List<Product> list;


        if (index == null || index == "0") //trang dau
        {
            index = "1";
        }

        int indexNumber = Integer.parseInt(index);
        if (cateID == null || cateID.isEmpty()) {
            cateID = "0";
        }
        int numPage;
        
        //xet TH cateID = 0 dan den phan countPageByID khong chay duoc
        
        
        if (cateID.equals("0")) {
            list = pd.getProductPagination(indexNumber, 9);
            numPage = pd.countPage(9);
            
        } else {
            list = pd.getProductPaginationByID(indexNumber, 9, cateID);
             numPage = pd.countPageByID(9, cateID);
        }
        request.setAttribute("indexPvsN", indexNumber);
        request.setAttribute("listp", list);
        request.setAttribute("listcc", listC);
        request.setAttribute("tag", cateID);
        request.setAttribute("numpage", numPage);
        request.getRequestDispatcher("home.jsp").forward(request, response);

    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
    }

